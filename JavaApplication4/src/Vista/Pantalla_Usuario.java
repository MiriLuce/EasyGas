    private  boolean esValidoCorreo(String correo) {
        boolean result = true;
        try {
           InternetAddress correoAd = new InternetAddress(correo);
           correoAd.validate();
        } catch (AddressException ex) {
           result = false;
        }
        return result;
    }    
    private String validar(String campo, String nombreCampo,int minCaracteres , int maxCaracteres ,String formato){
        // formato=d (numerico) formato=s solo cadenas
        
        String mensaje="";
        String regex = "[0-9]*"; 
        if (campo.compareTo("")== 0) {
            mensaje="El campo " + nombreCampo + " esta vacio";
            return mensaje;
        } 
        else{
            if (formato.compareTo("s")== 0) {
                if(campo.matches(regex)){
                    mensaje="El campo "+ nombreCampo + " solo debe contener letras";
                    return mensaje;
                }
                else{
                    if(!(maxCaracteres>=campo.length() && minCaracteres<=campo.length())) {
                        if(maxCaracteres==minCaracteres) mensaje="El campo " + nombreCampo + " debe de tener " + maxCaracteres + " caracteres" ;
                        else mensaje="El campo " + nombreCampo + " debe de tener entre " + minCaracteres + " y " + maxCaracteres + " caracteres";
                        return mensaje;
                    }
                }
            } else {
                
                if(!campo.matches(regex)){
                    mensaje="El campo "+ nombreCampo + " solo debe contener digitos";
                    return mensaje;
                }
                else{
                    if(!(maxCaracteres>=campo.length() && minCaracteres<=campo.length())) {
                        if(maxCaracteres==minCaracteres) mensaje="El campo " + nombreCampo + " debe de tener " + maxCaracteres + " caracteres" ;
                        else mensaje="El campo " + nombreCampo + " debe de tener entre " + minCaracteres + " y " + maxCaracteres + " caracteres";
                        return mensaje;
                    }
                }
                
            
            }
        } 
        return mensaje;
    }
    private void VerDatos(String strCodigo){    
        int idUsuarioSeleccionado = Integer.parseInt(strCodigo);
        Usuario usuario = usuarioControlador.obtenerUsuarioPorCodigo(idUsuarioSeleccionado);
        txtNombreCompleto.setText(usuario.getEmpleado().getNombres() + " " + usuario.getEmpleado().getApellidoPat() + " " + usuario.getEmpleado().getApellidoMat() );
        txtCorreo.setText(usuario.getCorreo());
        txtNombreUsuario.setText(usuario.getNombreUsuario());
        txtDNI.setText(usuario.getEmpleado().getDni());
        txtCodigo.setText(usuario.getIdUsuario().toString());
        
    }
