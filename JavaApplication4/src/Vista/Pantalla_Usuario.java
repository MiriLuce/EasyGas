    private void btnBuscarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEmpleadoActionPerformed

        String mensaje="";
        if(!((mensaje=validar(txtDNI.getText(),"DNI",8,8,"d")).compareTo("")== 0)) {
            JOptionPane.showMessageDialog(null,mensaje);
            return;
        }
        else {
            if ((empleado=empleadoControlador.obtenerEmpleadoPorDNI(txtDNI.getText()))==null){
                mensaje="EL DNI no pertenece a ningun Empleado";
                JOptionPane.showMessageDialog(null,mensaje);
                return;
            }
            else {
                Usuario usuarioPrueba = usuarioControlador.obtenerUsuarioPorIdEmpleado(empleado.getIdEmpleado());
                if(usuarioPrueba==null){
                    txtNombreCompleto.setText(empleado.getNombres()+ " " + empleado.getApellidoPat() + " " +empleado.getApellidoMat());
                    
                }
                else {
                    mensaje="EL DNI ya tiene una cuenta";
                    JOptionPane.showMessageDialog(null,mensaje);
                    return;

                }
            }

        }
    }//GEN-LAST:event_btnBuscarEmpleadoActionPerformed

    private void btnUsuarioGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUsuarioGuardarActionPerformed
        //validar campos vacios
         int confirmacion = JOptionPane.showConfirmDialog(null, "Seguro que desea guardar los cambios");
            if(confirmacion==0){
                 Usuario u=null;
            String mensaje="";
            if (!(txtCodigo.getText().compareTo("")==0)){
                // es actualizacion
                u=usuarioControlador.obtenerUsuarioPorCodigo(Integer.parseInt(txtCodigo.getText()));
            }
            if(!((mensaje=validar(txtCorreo.getText(),"Correo",5,50,"s")).compareTo("")== 0)) {
                JOptionPane.showMessageDialog(null,mensaje);
                return;
            }
            else {
                if (!esValidoCorreo(txtCorreo.getText())){
                    mensaje="El campo correo no tiene el formato correcto";
                    JOptionPane.showMessageDialog(null,mensaje);
                    return;
                }
                else {
                    if (( (txtCodigo.getText().compareTo("")==0)  || !((u.getCorreo().compareTo(txtCorreo.getText())==0))) && usuarioControlador.existeCorreo(txtCorreo.getText())){
                        mensaje="El correo ya ha sido registrado";
                        JOptionPane.showMessageDialog(null,mensaje);
                        return;
                    }

                }
            }

            if(!((mensaje=validar(txtNombreUsuario.getText(),"Nombre Usuario",5,15,"s")).compareTo("")== 0)) {
                JOptionPane.showMessageDialog(null,mensaje);
                return;
            }

            else {
                if ( ((txtCodigo.getText().compareTo("")==0) || !(u.getNombreUsuario().compareTo(txtNombreUsuario.getText())==0)) && usuarioControlador.existeNombreUsuario(txtNombreUsuario.getText(),"registro") ){
                    mensaje="El nombre de usuario ya existe";
                    JOptionPane.showMessageDialog(null,mensaje);
                    return;
                }

            }

            usuarioControlador.guardarUsuario(txtNombreUsuario.getText(),txtCorreo.getText(),empleado,txtCodigo.getText());

            mensaje = "Se guardaron los cambios exitosamente";
            List<Usuario> lista = usuarioControlador.listarUsuarios();
            usuarioControlador.actualizaTablaUsuarios(lista,this.tblUSuario);
            JOptionPane.showMessageDialog(null,mensaje);
        }
       

    }//GEN-LAST:event_btnUsuarioGuardarActionPerformed

