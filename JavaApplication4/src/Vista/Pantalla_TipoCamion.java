    private void btnTipoCamionGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTipoCamionGuardarActionPerformed
        int confirmacion = JOptionPane.showConfirmDialog(null, "Seguro que desea guardar los cambios");
        if (confirmacion== 0){ 
            TipoCamion tp = new TipoCamion();
            String strCodigo = txtTipoCamionCodigo.getText();
            tp.setNombre(txtTipoCamionNombre.getText());
            tp.setCapacidadGlp((int)spnTipoCamionGLP.getValue());
            tp.setCapacidadDiesel((int)spnTipoCamionDiesel.getValue());
            tp.setTara((int)spnTipoCamionTara.getValue());

            // Nuevo Registro de Tipo Camion
            if (strCodigo.equals("")){
                int verifica = tpCamionControlador.GuardarTipoCamion(tp);
                if (verifica==1){
                    DatosEditables(false);
                    BotonesEditables(false);
                    RefrescarTablaTipoCamion();
                }                
            }
            // Actualizacion de Registro de Tipo Camion
            else {
                tp.setIdTipoCamion(Integer.parseInt(strCodigo));
                int verifica = tpCamionControlador.ActualizarTipoCamion(tp);
                if (verifica==1){
                    DatosEditables(false);
                    BotonesEditables(false);
                    RefrescarTablaTipoCamion();
                } 
            }
        }      
    }//GEN-LAST:event_btnTipoCamionGuardarActionPerformed

    private void txtTipoCamionNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTipoCamionNombreKeyTyped
        // TODO add your handling code here:
        char car = evt.getKeyChar();
        if (txtTipoCamionNombre.getText().length() > 20) {
            evt.consume();
        }
        if (!(car >= 'a' && car <= 'z') && !(car >= 'A' && car <= 'Z') && (car < '0' || car > '9')) {
            evt.consume();
        }
    }//GEN-LAST:event_txtTipoCamionNombreKeyTyped


