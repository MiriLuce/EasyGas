    private class TipoCamionModelo extends AbstractTableModel{
        
	List<TipoCamion> listaTipoCamion = null;
	String [] titles = {"Código", "Nombre", "GLP", "Diesel", "Tara"};
	
        @Override
	public int getColumnCount() {
            // TODO Auto-generated method stub
            return titles.length;
	}
	@Override
	public int getRowCount() {
            // TODO Auto-generated method stub
            return listaTipoCamion.size();
	}
	@Override
	public Object getValueAt(int rowIndex, int columnIndex) {
            // TODO Auto-generated method stub
            TipoCamion tc = listaTipoCamion.get(rowIndex);
            String value = "";
            switch(columnIndex){
                case 0: value = tc.getIdTipoCamion().toString(); break;
                case 1: value = tc.getNombre(); break;
                case 2: value = ""+ tc.getCapacidadGlp(); break; 
                case 3: value = ""+ tc.getCapacidadDiesel(); break; 
                case 4: value = ""+ tc.getTara(); break; 
            }
            return value;
        }
	@Override	
	public String getColumnName(int col){
            return titles[col];
	}
    }

