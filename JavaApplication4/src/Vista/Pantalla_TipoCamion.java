    private void btnTipoCamionEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTipoCamionEliminarActionPerformed
        int confirmacion = JOptionPane.showConfirmDialog(null, "Seguro que desea eliminar el registro");
        if (confirmacion== 0){           
            String strCodigo = txtTipoCamionCodigo.getText();
            int codigo = Integer.parseInt(strCodigo);
            String mensaje = tpCamionControlador.EliminarTipoCamion(codigo);
            JOptionPane.showMessageDialog(null, mensaje);
            RefrescarTablaTipoCamion();
            txtTipoCamionCodigo.setText("");
            txtTipoCamionNombre.setText("");
            spnTipoCamionGLP.setValue(0);
            spnTipoCamionDiesel.setValue(0);
            spnTipoCamionTara.setValue(0);  
            DatosEditables(true);
            BotonesEditables(true);
        }
    }//GEN-LAST:event_btnTipoCamionEliminarActionPerformed

