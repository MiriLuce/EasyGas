    private void RefrescarTablaTipoCamion(){
        
        List<TipoCamion> tipoCamionLista = tipoCamionControlador.ListarTipoCamion();
        if (tipoCamionLista!=null){
            int cantidad = tipoCamionLista.size();
            for (int i=0; i< cantidad; i++){
                tblTipoCamion.setValueAt(tipoCamionLista.get(i).getIdTipoCamion(), i, 0);
                tblTipoCamion.setValueAt(tipoCamionLista.get(i).getNombre(), i, 1);
                tblTipoCamion.setValueAt(tipoCamionLista.get(i).getCapacidadGlp(), i, 2);
                tblTipoCamion.setValueAt(tipoCamionLista.get(i).getCapacidadDiesel(), i, 3);
                tblTipoCamion.setValueAt(tipoCamionLista.get(i).getTara(), i, 4);
            }
        }
    }
    
    private void RefrescarTablaCamion(List<Camion> camionLista ){
        
        if (camionLista!=null){
            int cantidad = camionLista.size();
            for (int i=0; i< cantidad; i++){
                tblTipoCamion.setValueAt(camionLista.get(i).getIdCamion(), i, 0);
                tblTipoCamion.setValueAt(camionLista.get(i).getPlaca(), i, 1);
                tblTipoCamion.setValueAt(camionLista.get(i).getFechaRegistro().toString(), i, 2);
                tblTipoCamion.setValueAt(camionLista.get(i).getTipoCamion().getNombre(), i, 3);                
                tblTipoCamion.setValueAt(camionLista.get(i).getEstado(), i, 4);
            }
        }
    }
    
