    private void VerDatos(String stringId){
        int id = Integer.parseInt(stringId);
        Cliente cli = clienteControlador.BuscaClienteId(id);
        txtCodigo.setText(""+cli.getIdCliente());
        cmbTipoDocumento.setSelectedItem(cli.getTipoDocumento());
        txtNumeroDoc.setText(cli.getNroDocumento());
        txtNombreComp.setText(cli.getNombres());
        cmbEstado.setSelectedItem(cli.getEstado());
        spinX.setValue(cli.getNodo().getCoordX());
        spinY.setValue(cli.getNodo().getCoordY());
    }
    
