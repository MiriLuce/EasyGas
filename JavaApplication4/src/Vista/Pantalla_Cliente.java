    //Modelo de la tabla
    private class ClienteModelo extends AbstractTableModel{

        String [] titles = {"Código","DNI","Nombre","Direccion","Fecha de Registro","Estado"};
        List<Cliente> lista = new ArrayList<Cliente>();
        
        @Override
        public int getRowCount() {
            //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            return lista.size();
        }

        @Override
        public int getColumnCount() {
            //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            return titles.length;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Object value = null;            
            switch(columnIndex){
                case 0: value = lista.get(rowIndex).getIdCliente(); break;
                case 1: value = lista.get(rowIndex).getNroDocumento();break;
                case 2: value = lista.get(rowIndex).getNombres(); break;
                case 3: value = "(" + lista.get(rowIndex).getNodo().getCoordX() + ";" + lista.get(rowIndex).getNodo().getCoordY() + ")"; break;
                case 4: value = lista.get(rowIndex).getFechaRegistro(); break;
                case 5: value = lista.get(rowIndex).getEstado(); break;
                                
            }
            return value;
        }
        
        @Override
        public String getColumnName(int col){
            return titles[col];
        }
        
    }
    
