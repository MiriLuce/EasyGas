    public void obtenerArchivo() {
        JFileChooser selector = new JFileChooser();
        selector.setFileFilter( new FileNameExtensionFilter("*.txt", "txt"));
        selector.setFileFilter( new FileNameExtensionFilter("*.csv", "csv"));        
        
        if(JFileChooser.APPROVE_OPTION == selector.showOpenDialog(null)){
            //this.setVisible(false);
            archivo = selector.getSelectedFile();
        }
        else archivo = null;
    }
    
    private void VerDatos(String strCodigo){
        int idCliente = Integer.parseInt(strCodigo);
        Cliente cli = cliControlador.BuscaClienteId(idCliente);
        txtCodigo.setText("" + cli.getIdCliente());
        txtTipoDoc.setText(cli.getTipoDocumento());
        txtNumeroDoc.setText(cli.getNroDocumento());
        txtNombreComp.setText(cli.getNombres());
        txtEstado.setText(cli.getEstado());
        txtX.setText("" + cli.getNodo().getCoordX());
        txtY.setText("" + cli.getNodo().getCoordY());
    }

