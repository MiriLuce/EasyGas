    private void btnClienteCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClienteCargarActionPerformed
        // TODO add your handling code here:
        List<Cliente> listaCliente = new ArrayList<Cliente>();
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        int[] datos = new int[2];
        try {
            cliControlador.CargaMasiva(archivo.getAbsolutePath(), datos, listaCliente);
            if (listaCliente.size()!=0){
                DefaultTableModel modelo = (DefaultTableModel) tablaCliente.getModel();
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
                modelo.setRowCount(0);
                for (int i = 0; i < listaCliente.size(); i++) {
                    Object[] fila = new Object[6];
                    fila[0] = listaCliente.get(i).getIdCliente();
                    fila[1] = listaCliente.get(i).getNroDocumento();
                    fila[2] = listaCliente.get(i).getNombres();
                    fila[3] = "(" + listaCliente.get(i).getNodo().getCoordX() + ";" + listaCliente.get(i).getNodo().getCoordY() + ")";
                    fila[4] = sdf.format(listaCliente.get(i).getFechaRegistro());
                    fila[5] = listaCliente.get(i).getEstado();
                    modelo.addRow(fila);
                }
                lblProcesados.setText("" + datos[1]);
                lblInvalidos.setText("" + datos[0]);
                btnClienteCargar.setEnabled(false);
                txtNombreArchivo.setText("");
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Pantalla_CamionCarga.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.setCursor(Cursor.getDefaultCursor());
    }//GEN-LAST:event_btnClienteCargarActionPerformed

