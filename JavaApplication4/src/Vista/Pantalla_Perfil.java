    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int confirmacion = JOptionPane.showConfirmDialog(null, "Seguro que desea guardar los cambios");
        if (confirmacion== 0){
            if(txtAnterior.getText().compareTo("")==0) 
            {
                JOptionPane.showMessageDialog(null, "El campo contrasenha actual esta vacio");
                return;
            }
            if(txtNueva.getText().compareTo("")==0) 
            {
                JOptionPane.showMessageDialog(null, "El campo contrasenha nueva esta vacio");
                return;
            }
            if(!(txtAnterior.getText().compareTo(usuarioControlador.descriptar(Modelo.Constantes.EasyGas.usuarioActual.getContrasenha()))==0) )
            {
                JOptionPane.showMessageDialog(null, "La contrasenha actual no es la correcta");
                return;
            }
            Modelo.Constantes.EasyGas.usuarioActual.setContrasenha(usuarioControlador.encriptar(txtNueva.getText()));
            usuarioControlador.actualizarContrasenha(Modelo.Constantes.EasyGas.usuarioActual.getIdUsuario().toString(),Modelo.Constantes.EasyGas.usuarioActual.getContrasenha() );
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

